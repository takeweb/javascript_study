/**
 * 指数関数
 * 種類: インスタンスメソッド
 * Math.exp():   指数関数(eの累乗)
 * Math.expm1(): 指数関数(eのn乗 −１) ex - 1 (x は引数、 e は自然対数の底) を返します
 *
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/exp
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/expm1
 *
 */
console.log(`指数関数(eの累乗): ${Math.exp(2)}`); // => 7.38905609893065
console.log(`指数関数(eのn乗 −１): ${Math.expm1(2)}`); // => 6.38905609893065

