/**
 * Mathの静的プロパティ
 * Math.E:       ネイピア数(オイラー数)
 * Math.LN2:     2の自然対数
 * Math.LN10:    10の自然対数
 * Math.LOG2E:   2を底としたネイピア数eの対数
 * Math.LOG10E:  10を底としたネイピア数eの対数
 * Math.PI:      円周率
 * Math.SQRT1_2: ½の平方根(または1/√2と等しい数)
 * Math.SQRT2:   2の平方根
 *
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/E
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/LN2
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/LN10
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/LOG2E
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/LOG10E
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/PI
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/SQRT1_2
 * https://developer.mozilla.org/ja/docs/Web/JavaScript/Reference/Global_Objects/Math/SQRT2
 */
console.log(`ネイピア数(Math.E): ${Math.E}`); // => 2.718281828459045
console.log(`2の自然対数(Math.LN2): ${Math.LN2}`); // => 0.6931471805599453
console.log(`10の自然対数(Math.LN10): ${Math.LN10}`); // => 2.302585092994046
console.log(`2を底としたEの対数(Math.LOG2E): ${Math.LOG2E}`); // => 1.4426950408889634
console.log(`10を底としたEの対数(Math.LOG10E): ${Math.LOG10E}`); // => 0.4342944819032518
console.log(`円周率(Math.PI): ${Math.PI}`); // => 3.141592653589793
console.log(`½の平方根(Math.SQRT1_2): ${Math.SQRT1_2}`); // => 0.7071067811865476
console.log(`2の平方根(Math.SQRT2): ${Math.SQRT2}`); // => 1.4142135623730951

